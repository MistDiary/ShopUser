package com.example.rgshopping.po;

public class User {

	private Integer user_id;
	private String user_name;
	private String tel1;
	private String password;
	private String v_money;
	private String z_money;
	private String u_image;
	
	public Integer getUser_id() {
		return user_id;
	}
	public void setUser_id(Integer user_id) {
		this.user_id = user_id;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getTel1() {
		return tel1;
	}
	public void setTel1(String tel1) {
		this.tel1 = tel1;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getV_money() {
		return v_money;
	}
	public void setV_money(String v_money) {
		this.v_money = v_money;
	}
	public String getZ_money() {
		return z_money;
	}
	public void setZ_money(String z_money) {
		this.z_money = z_money;
	}
	public String getU_image() {
		return u_image;
	}
	public void setU_image(String u_image) {
		this.u_image = u_image;
	}
	

}
