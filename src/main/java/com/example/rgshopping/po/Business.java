package com.example.rgshopping.po;

public class Business {
	private Integer bus_id;
	private String bus_image;
	private String bus_name;
	private String res_name;
	private String telphone;
	private String password;
	private String state;
	private String result;
	private String bus_intro;

	public Integer getBus_id() {
		return bus_id;
	}

	public void setBus_id(Integer bus_id) {
		this.bus_id = bus_id;
	}

	public String getBus_image() {
		return bus_image;
	}

	public void setBus_image(String bus_image) {
		this.bus_image = bus_image;
	}

	public String getBus_name() {
		return bus_name;
	}

	public void setBus_name(String bus_name) {
		this.bus_name = bus_name;
	}

	public String getRes_name() {
		return res_name;
	}

	public void setRes_name(String res_name) {
		this.res_name = res_name;
	}

	public String getTelphone() {
		return telphone;
	}

	public void setTelphone(String telphone) {
		this.telphone = telphone;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

	public String getBus_intro() {
		return bus_intro;
	}

	public void setBus_intro(String bus_intro) {
		this.bus_intro = bus_intro;
	}

}
